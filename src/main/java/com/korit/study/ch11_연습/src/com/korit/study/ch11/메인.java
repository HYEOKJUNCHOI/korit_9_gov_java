/**
 * 실행 시작점 (엔트리 포인트)
 */
public class 메인 {
    public static void main(String[] args) {
        // 학생 생성
        학생 학생1 = new 학생(20250001, "박재현", 24);
        학생 학생2 = new 학생(20250002, "고대한", 19);
        학생 학생3 = new 학생(20250003, "양재원", 27);

        // 학생 배열
        학생[] 학생들 = { 학생1, 학생2, 학생3 };

        // 선생님과 과목 생성
        선생님 김선생님 = new 선생님("김준일", 32);
        과목 자바기초 = new 과목("JAVA 기초", 김선생님);

        // 스터디 생성
        스터디 자바스터디 = new 스터디(자바기초, 학생들);

        // 출력 서비스 생성 및 전체 출력
        스터디출력서비스 출력기 = new 스터디출력서비스();
        출력기.전체정보출력(자바스터디);
    }
}
